package com.xcode.javas.durgasoft._b_flowcontrol;

public class SwitchCase
{
    public static void main(String[] args)
    {
        final int x=10;
        switch (x){
            default:
                System.out.println("Hello");
            case x:
                System.out.println("Hi");

        }
        byte b=10;
        switch (b)
        {
            case 100:
                System.out.println("Hi");

        }

    }
}
