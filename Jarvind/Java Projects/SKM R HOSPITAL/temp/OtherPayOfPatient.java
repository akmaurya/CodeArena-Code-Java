	private boolean save()
	{
		int pid=0;
		double amt=0;
		if(jTextField1.getText().equals(""))
		{
			JOptionPane.showMessageDialog(null,"Please Input Patient ID");
			jTextField1.requestFocus();
			return false;
		}
		if(jComboBox1.getSelectedItem()==null)
		{
			JOptionPane.showMessageDialog(null,"No Purpose Available\nPlease Input Purpose");
			jComboBox1.requestFocus();
			return false;
		}
		if(jTextField2.getText().equals(""))
		{
			JOptionPane.showMessageDialog(null,"Please Input Amount");
			jTextField2.requestFocus();
			return false;
		}
		try
		{
			pid=Integer.parseInt(jTextField1.getText());
		}
		catch(Exception e)
		{
			JOptionPane.showMessageDialog(null,"Please Input Correct Patient ID ( Numbers Only )");
			jTextField1.requestFocus();
			jTextField1.selectAll();
			return false;
		}
		try
		{
			amt=Double.valueOf(jTextField2.getText());
		}
		catch(Exception e)
		{
			JOptionPane.showMessageDialog(null,"Please Input Correct Amount ( Numbers Only )");
			jTextField2.requestFocus();
			jTextField2.selectAll();
			return false;
		}
		try
		{
			//ZedInterface inf=(ZedInterface)Naming.lookup(getIPaddress.IP());
try
							{
								inf=(ZedInterface)Naming.lookup(getIPaddress.IP());
				
				
							}
							catch(Exception e)
							{
									System.out.println("Error at the time of connection with server "+getIPaddress.IP()+"..."+e);
									getIPaddress.changeIP();
									try
									{
										inf=(ZedInterface)Naming.lookup(getIPaddress.IP());
											
										
									}
									catch(Exception ex)
									{
										System.out.println("Error at the time of connection with server ip-rmi//127.0.0.1/ExtendZedServer  ..."+ex);
									}
							}				
			java.util.Date dt=new java.util.Date();
			String curDt=(dt.getMonth()+1)+"/"+(dt.getDate())+"/"+(dt.getYear()+1900);
			inf.executeUpdateQuery("insert into other_Payment_Detail values("+pid+",'"+""+jComboBox1.getSelectedItem()+"',"+amt+",#"+curDt+"#,"+false+")");
			JOptionPane.showMessageDialog(null,"Data Saved Successfully..");
			jTextField1.setText("");
			jTextField2.setText("");
			jTextField1.requestFocus();
		}
		catch(Exception e)
		{
			System.out.println("Error in save method of OtherPayOfPayment class..."+e);
		}
		return false;
	}
