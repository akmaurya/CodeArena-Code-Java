    public boolean calculationOfAmt()
    {
        double frameCharge=0,glassCharge=0,otherCharge=0,advance=0;
        if(jTextField1.getText().equals(""))
        {
            frameCharge=0;
        }
        else
        {
            try
            {
                frameCharge=Double.valueOf(jTextField1.getText());
            }
            catch(Exception e)
            {
                //System.out.println("Error..SKM R....."+e);
                javax.swing.JOptionPane.showMessageDialog(this,""+e+"\nPlease Input Correct Frame Charge ?","Error !!",javax.swing.JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.selectAll();
                return false;
            }
        }
        if(jTextField2.getText().equals(""))
        {
            glassCharge=0;
        }
        else
        {
            try
            {
                glassCharge=Double.valueOf(jTextField2.getText());
            }
            catch(Exception e)
            {
                //System.out.println("Error..SKM R....."+e);
                javax.swing.JOptionPane.showMessageDialog(this,""+e+"\nPlease Input Correct Glass Charge ?","Error !!",javax.swing.JOptionPane.ERROR_MESSAGE);
                jTextField2.requestFocus();
                jTextField2.selectAll();
                return false;
            }
        }
        if(jTextField3.getText().equals(""))
        {
            otherCharge=0;
        }
        else
        {
            try
            {
                otherCharge=Double.valueOf(jTextField3.getText());
            }
            catch(Exception e)
            {
                //System.out.println("Error..SKM R....."+e);
                javax.swing.JOptionPane.showMessageDialog(this,""+e+"\nPlease Input Correct Extra Charge ?","Error !!",javax.swing.JOptionPane.ERROR_MESSAGE);
                jTextField3.requestFocus();
                jTextField3.selectAll();
                return false;
            }
        }
        if(jTextField4.getText().equals(""))
        {
            advance=0;
        }
        else
        {
            try
            {
                advance=Double.valueOf(jTextField4.getText());
            }
            catch(Exception e)
            {
                ////System.out.println("Error..SKM R....."+e);
                javax.swing.JOptionPane.showMessageDialog(this,""+e+"\nPlease Input Correct Advace Payment ?","Error !!",javax.swing.JOptionPane.ERROR_MESSAGE);
                jTextField4.requestFocus();
                jTextField4.selectAll();
                return false;
            }
        }
        double totalAmt=frameCharge+glassCharge+otherCharge;
        jLabel6.setText(""+(new java.math.BigDecimal(totalAmt).setScale(2,java.math.BigDecimal.ROUND_HALF_UP)));
        double dueAmt=totalAmt-advance;
        jLabel10.setText(""+(new java.math.BigDecimal(dueAmt).setScale(2,java.math.BigDecimal.ROUND_HALF_UP)));
        return true;
    }
