	private boolean save()
	{
		if(jTextField1.getText().trim().equals(""))
		{
			JOptionPane.showMessageDialog(null,"Please Enter Admin Id ?","Error !!!",JOptionPane.ERROR_MESSAGE);
			jTextField1.requestFocus();
			return false;
		}
		if(jPasswordField1.getText().trim().equals(""))
		{
			JOptionPane.showMessageDialog(null,"Please Enter Admin password ?","Error !!!",JOptionPane.ERROR_MESSAGE);
			jPasswordField1.requestFocus();
			return false;
		}
		if(jTextField2.getText().trim().equals(""))
		{
			JOptionPane.showMessageDialog(null,"Please Enter User Id ?","Error !!!",JOptionPane.ERROR_MESSAGE);
			jTextField2.requestFocus();
			return false;
		}
		if(jPasswordField2.getText().trim().equals(""))
		{
			JOptionPane.showMessageDialog(null,"Please Enter User password ?","Error !!!",JOptionPane.ERROR_MESSAGE);
			jPasswordField2.requestFocus();
			return false;
		}
		try
		{
			ZedInterface inf=null;
ZedInterfaceForMedical uc=null;
try
{
	inf=(ZedInterface)Naming.lookup(getIPaddress.IP());
	uc=(ZedInterfaceForMedical)Naming.lookup(getIPaddress.IP());
}
catch(Exception e)
{
	System.out.println("Error with connection with server "+getIPaddress.IP()+" ...");
	getIPaddress.changeIP();
	try
	{
		inf=(ZedInterface)Naming.lookup(getIPaddress.IP());
		uc=(ZedInterfaceForMedical)Naming.lookup(getIPaddress.IP());
	}
	catch(Exception ex)
	{
		System.out.println("Error with connection with server  rmi://127.0.0.1/ExtendZedServer...");
	}
}
			String check1=inf.getSingleValue("select uLevel from loginMasterOfMedical where uId='"+jTextField1.getText()+"' and uPws='"+jPasswordField1.getText()+"'");
			if(check1==null)
			{
				JOptionPane.showMessageDialog(null,"Please Enter Correct Admin Id / Password","Error !!!",JOptionPane.ERROR_MESSAGE);
				jTextField1.requestFocus();
				jTextField1.selectAll();
				return false;
			}
			else if(check1.equals("0"))
			{
				String mnuItem="";
				check1=inf.getSingleValue("select uId from loginMasterOfMedical where uId='"+jTextField2.getText()+"'");
				System.out.println("check1  "+check1);
				if(check1!=null)
				{
					JOptionPane.showMessageDialog(null,"User ID Existed Before","Error !!!",JOptionPane.ERROR_MESSAGE);
					jTextField2.requestFocus();
					jTextField2.selectAll();
					return false;
				}
				else
				{
					System.out.println("total number of items  "+vc2.size());
					for(int i=0;i<vc2.size();i++)
					{
						mnuItem+=vc2.elementAt(i)+",";
					}
					System.out.println(""+mnuItem);
					String qry="insert into loginMasterOfMedical values('"+jTextField1.getText()+"','"+jTextField2.getText()+"','"+jPasswordField2.getText()+"',1,'"+mnuItem+"')";
					System.out.println("qry  "+qry);
					inf.executeUpdateQuery("insert into loginMasterOfMedical values('"+jTextField1.getText()+"','"+jTextField2.getText()+"','"+jPasswordField2.getText()+"',1,'"+mnuItem+"')");
					JOptionPane.showMessageDialog(null,"User Created Successfully","Congratulation !!!",JOptionPane.INFORMATION_MESSAGE);
					jButton2.setEnabled(false);
					jButton3.requestFocus();
				}	
			}
		}
		catch(Exception e)
		{
			System.out.println("Error in save method of CreateNewUser class..."+e);
		}
		return true;
	}
