    private void getDataFromDB()
    {
        String data="";
        boolean flag=true;
        try
        {
            String query="select * from firmProfile";
            /*ConnectDatabase cd=new ConnectDatabase();
            java.sql.Connection connection = cd.getConnection();
            java.sql.ResultSet rs;
            java.sql.Statement st=connection.createStatement();*/
            ZedInterface inf=null;
ZedInterfaceForMedical uc=null;
try
{
	inf=(ZedInterface)Naming.lookup(getIPaddress.IP());
	uc=(ZedInterfaceForMedical)Naming.lookup(getIPaddress.IP());
}
catch(Exception e)
{
	System.out.println("Error with connection with server "+getIPaddress.IP()+" ...");
	getIPaddress.changeIP();
	try
	{
		inf=(ZedInterface)Naming.lookup(getIPaddress.IP());
		uc=(ZedInterfaceForMedical)Naming.lookup(getIPaddress.IP());
	}
	catch(Exception ex)
	{
		System.out.println("Error with connection with server  rmi://127.0.0.1/ExtendZedServer...");
	}
}
            
            String[][] rs=inf.getResultSetAs_2D_Array(query,5);
            
            if(rs!=null)
            {
                data=rs[0][0];
                if(data==null || data.equals("null") || data.equals(""))
                {
                    jTextField1.requestFocus();
                    flag=false;
                }
                else
                {
                    jTextField1.setText(""+data);
                    jTextField1.setEditable(false);
                    data="";
                }
                data=rs[0][1];
                if(data==null || data.equals("null") || data.equals(""))
                {
                    if(flag)
                    {
                        jTextField2.requestFocus();
                        flag=false;
                    }
                    
                }
                else
                {
                    jTextField2.setText(""+data);
                    jTextField2.setEditable(false);
                    data="";
                }
                data=rs[0][2];
                if(data==null || data.equals("null") || data.equals(""))
                {
                    if(flag)
                    {
                        jTextField3.requestFocus();
                        flag=false;
                    }
                }
                else
                {
                    jTextField3.setText(""+data);
                    jTextField3.setEditable(false);
                    data="";
                   // if(flag)
                   // jButton2.requestFocus();
                }
                data=rs[0][3];
                if(data==null || data.equals("null") || data.equals(""))
                {
                    if(flag)
                    {
                        jTextField4.requestFocus();
                        flag=false;
                    }
                    
                }
                else
                {
                    jTextField4.setText(""+data);
                    jTextField4.setEditable(false);
                    data="";
                }
                data=rs[0][4];
                if(data==null || data.equals("null") || data.equals(""))
                {
                    if(flag)
                    {
                        jTextField5.requestFocus();
                        flag=false;
                    }
                    
                }
                else
                {
                    jTextField5.setText(""+data);
                    jTextField5.setEditable(false);
                    data="";
                    if(flag)
                    jButton2.requestFocus();
                }
            }
            
            
        }
        catch(Exception e)
        {
            System.out.println("Error getDataFromDB method.."+e);
        }
    }
