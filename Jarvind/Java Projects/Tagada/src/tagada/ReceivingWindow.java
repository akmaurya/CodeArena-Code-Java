private void loadAmt()
{
    String qry="";
    java.math.BigDecimal bd=null;
    try
    {
        qry="Select debit,credit from party_Account where party_name='"+jComboBox3.getSelectedItem().toString()+"'";
        String getAmt[][]=Utility.getInformationDD(qry,2);
        if(getAmt.length>0)
        {
            double debit=Double.valueOf(getAmt[0][0]);
            double credit=Double.valueOf(getAmt[0][1]);
            debit=debit-credit;
            bd=new java.math.BigDecimal(""+debit);
            bd.abs();
            if(debit<0)
                jLabel11.setVisible(true);
            else
                jLabel11.setVisible(false);
            jLabel9.setText(""+bd.setScale(2,5));
        }
    }
    catch(Exception e)
    {
        System.out.println("Error ! In loadAmt Method."+e);
    }
}
private boolean Save()
{
    String qry="";
    try
    {
        double temp=Double.valueOf(jLabel9.getText());

        if(Utility.isBlank(jTextField1.getText().trim())==true)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter the Booking Number.");
            jTextField1.requestFocus();
            return false;
        }
        if(Utility.isInteger(jTextField2.getText().trim())==false)
        {
            javax.swing.JOptionPane.showMessageDialog(null,"Please enter in the Integer format.");
            jTextField2.requestFocus();
            return false;
        }
        if(Utility.isBlank(jTextField2.getText().trim())==true)
        {
            javax.swing.JOptionPane.showMessageDialog(this,"Please enter the Receipt Number.");
            jTextField2.requestFocus();
            return false;
        }
        if(Utility.isInteger(jTextField2.getText().trim())==false)
        {
            javax.swing.JOptionPane.showMessageDialog(null,"Please enter in the Integer format.");
            jTextField2.requestFocus();
            return false;
        }
        if(Utility.isBlank(jTextField3.getText().trim())==true)
        {
            javax.swing.JOptionPane.showMessageDialog(this,"Please enter the Received Amount.");
            jTextField3.requestFocus();
            return false;
        }
        if(Utility.isInteger(jTextField3.getText().trim())==false)
        {
            javax.swing.JOptionPane.showMessageDialog(null,"Please enter in the Number format.");
            jTextField3.requestFocus();
            return false;
        }
        if(temp==0)
        {
            javax.swing.JOptionPane.showMessageDialog(null,"Sorry ! Debit Amount is Nill.");
            jTextField1.requestFocus();
            jTextField1.selectAll();
            return false;
        }

        qry="Select receipt_no from receiving_Detail where booking_no='"+jTextField1.getText().trim()+"' and receipt_no="+Integer.parseInt(jTextField2.getText().trim())+"";
        String ReceiptNo=Utility.getSingleValue(qry);
        if(ReceiptNo.length()>0)
        {
            javax.swing.JOptionPane.showMessageDialog(null,"Sorry ! this receipt no. already exists.");
            jTextField2.requestFocus();
            jTextField2.selectAll();
            return false;
        }

        String D[]=jLabel5.getText().split("-");
        String setDate=D[1]+"-"+D[0]+"-"+D[2];

        qry="select staff_code from staff_Detail where staff_name='"+jComboBox1.getSelectedItem().toString()+"'";
        String scode=Utility.getSingleValue(qry);

        qry="select area_name from area_Master where route_nm='"+jComboBox2.getSelectedItem().toString()+"'";
        String areaName[][]=Utility.getInformationDD(qry,1);
        String area="";
        if(areaName.length==0)
        {
            javax.swing.JOptionPane.showMessageDialog(null,"Sorry ! there is no any area found in this route..");
            jComboBox2.requestFocus();
            return false;

        }
        else
        {
            for(int i=0;i<areaName.length;i++)
            {
                if(area.equals("")==true)
                    area=areaName[i][0];
                else
                    area=area+", "+areaName[i][0];
            }
        }
        double partyAmount=Double.valueOf(jLabel9.getText());
        double receivingAmount=Double.valueOf(jTextField3.getText().trim());
        double debitAmount=0,creditAmount=0;
        if(receivingAmount>partyAmount)
        {
           creditAmount=receivingAmount-partyAmount;
           debitAmount=0.0;
        }
        else
        {
           debitAmount=partyAmount-receivingAmount;
           creditAmount=0;
        }

        qry="insert into receiving_Detail values('"+jTextField1.getText().trim()+"',"+Integer.parseInt(jTextField2.getText().trim())+",#"+setDate+"#,"+Integer.parseInt(scode)+",'"+jComboBox2.getSelectedItem().toString()+"','"+area+"','"+jComboBox3.getSelectedItem().toString()+"',"+Double.valueOf(jLabel9.getText())+","+Double.valueOf(jTextField3.getText().trim())+")";
        boolean bn=Utility.saveInformation(qry);
        qry="update party_Account set debit="+debitAmount+" ,credit="+creditAmount+" where party_name='"+jComboBox3.getSelectedItem().toString()+"'";
        bn=Utility.saveInformation(qry);
        if(bn)
        {
            javax.swing.JOptionPane.showMessageDialog(null,"Process Completed.");

            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("00.00");

            uploadDetail();
            int receipt_No=Integer.parseInt(jTextField2.getText().trim())+1;
            jTextField2.setText(""+receipt_No);
            jTextField3.setText("");
            jComboBox3.setSelectedIndex(0);
            java.util.Date dt=new java.util.Date();
            int date=dt.getDate();
            int month=dt.getMonth()+1;
            int year=dt.getYear()+1900;
            setDate=date+"-"+month+"-"+year;
            jLabel5.setText(setDate);

            jTextField1.requestFocus();
        }
    }
    catch(Exception e)
    {
        System.out.println("Error ! In Save Method."+e);
    }
    return true;

}
