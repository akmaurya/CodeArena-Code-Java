private void uplaodDetail()
{
    String qry="";
    try
    {
        qry="select route_nm from route_Master order by route_nm asc";
        
        String load[][]=Utility.getInformationDD(qry,1);
        String arr[]=null;
        if(load.length>0)
        {
            arr=new String[2];
            for(int i=0;i<load.length;i++)
            {
                arr[0]=""+(dtm.getRowCount()+1)+".";
                arr[1]=load[i][0];
                dtm.addRow(arr);
            }
        }
    }
    catch(Exception e)
    {
        System.out.println("Error ! In loadInitialInfo Method."+e);
    }

}
private void saveDetail()
{
    String route=jTextField1.getText();
    String routecode=route.substring(0, 1).toUpperCase()+route.substring(1);
    String qry="";
    
    try
    {
        qry="select route_nm from route_Master where route_nm='"+(jTextField1.getText())+"'";
        String str=Utility.getSingleValue(qry);
        if(str.length()>0)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Route Name already exists! Please enter another Route Name.");
            jTextField1.requestFocus();
        }

        else
        {
            qry="insert into route_Master values('"+routecode+"')";
            Utility.saveInformation(qry);
            javax.swing.JOptionPane.showMessageDialog(this, "Congratulations! New Route created.");
            jTextField1.requestFocus();
            jTextField1.setText("");

            qry="select route_nm from route_Master order by route_nm asc";

            String load[][]=Utility.getInformationDD(qry,1);
            String arr[]=null;
            int c=load.length-1;

            arr=new String[2];

            arr[0]=""+(c+1)+".";
            arr[1]=load[c][0];
            dtm.addRow(arr);
        }
    }
    catch(Exception ae)
    {
        System.out.println(ae);
    }
}

private void deleteRoute()
{
    String qry="";

    try
    {
        if(jTable1.getSelectedRowCount()<1)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please! Select a Route Name.");
        }

        else if(dtm.getRowCount() > 0)
        {
            int c=javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this Route Name?", "Delete Route Name", javax.swing.JOptionPane.YES_NO_OPTION);
            if(c==javax.swing.JOptionPane.YES_OPTION)
            {
                qry="delete * from route_Master where route_nm='"+jTable1.getValueAt(jTable1.getSelectedRow(), 1)+"'";
                Utility.saveInformation(qry);
                
                dtm.setRowCount(0);
                uplaodDetail();

                javax.swing.JOptionPane.showMessageDialog(this, "Route Name Deleted Successfully.");

                jTextField1.setText("");
                jTextField1.requestFocus();
            }
        }
    }

    catch(Exception ae)
    {
        System.out.println(ae);
    }
}

private void setRoute()
{
    int i=jTable1.getSelectedRow();
    jTextField1.setText(""+jTable1.getValueAt(i, 1));
}

private void updateRoute()
{
    String qry="";

    if(jTable1.getSelectedRowCount()<1)
    {
        javax.swing.JOptionPane.showMessageDialog(this, "Record is not selected.");
    }

    else if(dtm.getRowCount() > 0)
    {
        if(jTextField1.getText().equals(""))
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter the Route Name.");
            jTextField1.requestFocus();
        }
        
        qry="select route_nm from route_Master where route_nm='"+(jTextField1.getText())+"'";
        String str=Utility.getSingleValue(qry);

        if(str.length()>0)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Route Name already exists! Please enter another Route Name.");
            jTextField1.requestFocus();
        }

        else
        {
            int c=javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to update this Route Name?", "Update Route Name", javax.swing.JOptionPane.YES_NO_OPTION);
            if(c==javax.swing.JOptionPane.YES_OPTION)
            {
                qry="update route_Master set route_nm='"+jTextField1.getText().trim()+"' where route_nm='"+(""+jTable1.getValueAt(jTable1.getSelectedRow(), 1))+"'";
                Utility.saveInformation(qry);

                dtm.setRowCount(0);
                uplaodDetail();
                
                javax.swing.JOptionPane.showMessageDialog(this, "Route Name Updated Successfully.");
            }
        }
    }
}
