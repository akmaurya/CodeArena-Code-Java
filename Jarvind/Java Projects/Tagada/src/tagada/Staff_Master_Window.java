boolean Save()
{
    String qry="";
    try
    {
        if(Utility.isBlank(jTextField1.getText().trim())==true)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter Staff Name.");
            jTextField1.requestFocus();
            return false;
        }
        if(Utility.isBlank(jTextField4.getText().trim())==true)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please Input Staf Age.");
            jTextField4.requestFocus();
            return false;
        }
        if(Utility.isBlank(jTextField3.getText().trim())==true && Utility.isBlank(jTextField6.getText().trim())==true)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter Staff's Contact No.");
            jTextField3.requestFocus();
            return false;
        }
        if(jComboBox4.getSelectedIndex()==0)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select Staff's Qualification.");
            jComboBox4.requestFocus();
            return false;
        }
        if(Utility.isBlank(jTextField5.getText().trim())==true)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please specify Staff's Qualification.");
            jTextField5.requestFocus();
            return false;
        }

        int ind=0;
        qry="select max(staff_code) from staff_Detail";
        String getMax=Utility.getSingleValue(qry);
        if(getMax!=null)
        {
            ind=Integer.parseInt(getMax)+1;
        }
        else
        {
            ind=1;
        }

        String Ph="";
        if(jTextField3.getText().equals("")==false && jTextField6.getText().equals("")==false)
        {
            Ph=jTextField3.getText().trim()+", "+jTextField6.getText().trim();
        }

        if(jTextField3.getText().equals("")==false && jTextField6.getText().equals("")==true)
        {
            Ph=jTextField3.getText().trim();
        }

        if(jTextField3.getText().equals("")==true && jTextField6.getText().equals("")==false)
        {
            Ph=jTextField6.getText().trim();
        }

        String D[]=jLabel5.getText().split("-");
        String setDate=D[1]+"-"+D[0]+"-"+D[2];
        String Qualification="";
        if(jTextField5.isEnabled()==true)
            Qualification=jTextField5.getText().trim();
        else
            Qualification=jComboBox4.getSelectedItem().toString();

        qry="insert into staff_Detail values("+ind+",'"+jTextField1.getText().trim()+"','"+jTextField2.getText().trim()+"','"+Ph+"',#"+setDate+"#,'"+Qualification+"')";
        boolean bn=Utility.saveInformation(qry);
        if(bn)
        {
            javax.swing.JOptionPane.showMessageDialog(null,"Process Completed.");
            uploadDetail();
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jComboBox4.setSelectedIndex(0);
            jTextField1.requestFocus();

        }
    }
    catch(Exception e)
    {
        System.out.println("Error ! In Save Method."+e);
    }
   return true;
}

private void uploadDetail()
{
    String qry="";
    dtm.setRowCount(0);
    try
    {
        dtm.setRowCount(0);

        qry="Select * from staff_Detail order by staff_code asc";
        String arr[][]=Utility.getInformationDD(qry,6);

        String arr2[]=new String[7];

        if(arr.length>0)
        {
            for(int i=0;i<arr.length;i++)
            {
                arr2[0]=""+(dtm.getRowCount()+1)+".";
                arr2[1]=arr[i][1];
                arr2[2]=arr[i][2];
                arr2[3]=arr[i][3];

                String D[]=arr[i][4].split(" ");
                String D1[]=D[0].split("-");
                String setDate=D1[2]+"-"+D1[1]+"-"+D1[0];
                arr2[4]=setDate;

                arr2[5]=arr[i][5];
                arr2[6]=arr[i][0];
                dtm.addRow(arr2);
            }
        }
    }
    catch(Exception e)
    {
        System.out.println("Error ! In uploadDetail Method."+e);
    }
}

