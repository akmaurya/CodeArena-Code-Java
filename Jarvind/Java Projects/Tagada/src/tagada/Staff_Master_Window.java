private void setStaff()
{
    int i=jTable1.getSelectedRow();
    jTextField1.setText(""+jTable1.getValueAt(i, 1));
    jTextField2.setText(""+jTable1.getValueAt(i, 2));

    String  str=""+jTable1.getValueAt(i, 3);
    String arr[]=str.split(",");
    if(arr.length==1)
    {
        jTextField3.setText(arr[0].trim());
        jTextField6.setText("");
    }
    else
    {
        jTextField3.setText(arr[0].trim());
        jTextField6.setText(arr[1].trim());
    }

    jLabel5.setText(""+jTable1.getValueAt(i, 4));
    jTextField5.setText(""+jTable1.getValueAt(i, 5));
}

private void deleteStaff()
{
    String qry="";

    try
    {
        if(jTable1.getSelectedRowCount()<1)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please! Select a Staff Record.");
        }

        else if(dtm.getRowCount() > 0)
        {
            int c=javax.swing.JOptionPane.showConfirmDialog(this, "This may reflect to other details also. Are you sure?", "Delete Staff Record", javax.swing.JOptionPane.YES_NO_OPTION);
            if(c==javax.swing.JOptionPane.YES_OPTION)
            {
                qry="delete * from staff_Detail where staff_name='"+jTable1.getValueAt(jTable1.getSelectedRow(), 1)+"'";
                Utility.saveInformation(qry);

                dtm.removeRow(jTable1.getSelectedRow());
                uploadDetail();
                javax.swing.JOptionPane.showMessageDialog(this, "Staff Name Deleted Successfully.");

                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jComboBox4.setSelectedIndex(0);
                jTextField1.requestFocus();
            }
        }
    }

    catch(Exception ae)
    {
        System.out.println(ae);
    }
}

private void updateStaff()
{
    String qry="";

    if(jTable1.getSelectedRowCount()<1)
    {
        javax.swing.JOptionPane.showMessageDialog(this, "Record is not selected.");
    }

    else if(dtm.getRowCount() > 0)
    {
        if(jTextField1.getText().equals(""))
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter the Staff Name.");
            jTextField1.requestFocus();
        }

        else if(jTextField2.getText().equals(""))
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter the Address.");
            jTextField2.requestFocus();
        }

        else if(jTextField3.getText().equals("") && jTextField4.getText().equals(""))
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter the Phone Number.");
            jTextField3.requestFocus();
        }

        else if(jTextField4.getText().equals(""))
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter the Staff Age.");
            jTextField4.requestFocus();
        }

        else if(jLabel5.getText().equals(""))
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a Date.");
            jButton1.requestFocus();
        }

        else if(jComboBox4.getSelectedIndex() == 0)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select Staff Qualification.");
            jComboBox4.requestFocus();
        }

        else
        {
            int c=javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to update this Staff Detail?", "Update Sale Detail", javax.swing.JOptionPane.YES_NO_OPTION);
            if(c==javax.swing.JOptionPane.YES_OPTION)
            {
                String Ph="";
                if(jTextField3.getText().equals("")==false && jTextField6.getText().equals("")==false)
                {
                    Ph=jTextField3.getText().trim()+", "+jTextField6.getText().trim();
                }

                if(jTextField3.getText().equals("")==false && jTextField6.getText().equals("")==true)
                {
                    Ph=jTextField3.getText().trim();
                }

                if(jTextField3.getText().equals("")==true && jTextField6.getText().equals("")==false)
                {
                    Ph=jTextField6.getText().trim();
                }

                String str=jLabel5.getText().trim();
                String D[]=str.split("-");
                String setDate=D[1]+"-"+D[0]+"-"+D[2];

                qry="update staff_Detail set staff_name='"+jTextField1.getText().trim()+"', address='"+jTextField2.getText().trim()+"', phone_no='"+Ph+"', DOJ=#"+setDate+"# where staff_name='"+(""+jTable1.getValueAt(jTable1.getSelectedRow(), 1))+"'";
                Utility.saveInformation(qry);

                dtm.setRowCount(0);
                uploadDetail();

                javax.swing.JOptionPane.showMessageDialog(this, "Staff Detail Updated Successfully.");
            }
        }
    }
}
