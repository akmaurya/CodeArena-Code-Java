        // TODO add your handling code here:
         String purjaNumber=javax.swing.JOptionPane.showInputDialog(this,"Enter purja number." ,javax.swing.JOptionPane.INFORMATION_MESSAGE);
         int flag=0;
         if(purjaNumber.isEmpty()==false)
         {
             if(Utility.isInteger(purjaNumber))
             {
                     String qry="select purja_number,status from polish_details where purja_number="+(Integer.parseInt(purjaNumber))+"";
                     String data[][]=Utility.getInformationDD(qry,2);
                     
                     for(int i=0;i<data.length;i++)
                     {
                         if((data[i][0]).equals(""+purjaNumber))
                         {
                             if(Integer.parseInt(data[i][1])==2)
                             {
                                flag=2;
                                break;
                             }
                             if(Integer.parseInt(data[i][1])!=2)
                             {
                                flag=1;
                                break;
                             }
                         }
                     }
                    if(flag==1)
                    {
                        
                    Edit_Police_customer_work ob=new  Edit_Police_customer_work(Integer.parseInt(purjaNumber));
                    ob.setBounds(50,50,947, 635);
                    jDesktopPane1.add(ob);
                    ob.setVisible(true);
                    }
                    else if(flag==2)
                    {
                          javax.swing.JOptionPane.showMessageDialog(this," This purja is deliver, updation is not possible. " ,"Message Box",javax.swing.JOptionPane.INFORMATION_MESSAGE);

                    }
                    else
                    {
                         javax.swing.JOptionPane.showMessageDialog(this,"This purja number does not exist." ,"Message Box",javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    }
             }
         }

