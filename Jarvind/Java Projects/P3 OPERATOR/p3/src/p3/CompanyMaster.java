    public boolean save()
    {
        try
        {
            if(jTextField1.getText().isEmpty())
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Please enter company name.");
                jTextField1.requestFocus();
                return false;
            }

            else if(jComboBox1.getSelectedIndex()==0)
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Please select financial year.");
                jComboBox1.requestFocus();
                return false;
            }

            else if(jTextField2.getText().isEmpty())
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Please enter date.");
                jTextField2.requestFocus();
                return false;
            }

            else if(jTextField3.getText().isEmpty())
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Please enter user id.");
                jTextField3.requestFocus();
                return false;
            }

            else if(jPasswordField1.getEchoChar()==0)
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Please enter password.");
                jPasswordField1.requestFocus();
                return false;
            }

            else
            {
                //to be edited
            }

            boolean success = false;
            // Creating new directory in Java, if it doesn't exists
            java.io.File directory = new java.io.File("C:\\mysql\\data"+jTextField1.getText().trim()+jComboBox1.getSelectedItem().toString());
            if(directory.exists())
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Company name already created.");
            }
            else
            {
                System.out.println("Company not exists, creating now");
                success = directory.mkdir();
                
                if(success)
                {
                    javax.swing.JOptionPane.showMessageDialog(this, "Congratulation! company name successfully created.");
                }
                else
                {
                    javax.swing.JOptionPane.showMessageDialog(this, "Error! company name not created.");
                }
            }
        }
        catch(Exception ae)
        {
            System.out.println("Eror in save method"+ae);
        }
        return true;
    }
