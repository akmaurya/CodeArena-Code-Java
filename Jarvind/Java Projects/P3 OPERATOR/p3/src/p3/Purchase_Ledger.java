public void saveValidation()
{
    if(jTextField1.getText().isEmpty())
    {
        javax.swing.JOptionPane.showMessageDialog(this, "Error! Please enter invoice number.");
        jTextField1.requestFocus();
    }
    else if(jFormattedTextField1.getText().isEmpty())
    {
        javax.swing.JOptionPane.showMessageDialog(this, "Error! Please enter entry date.");
        jFormattedTextField1.requestFocus();
    }
    else if(jTextField3.getText().isEmpty())
    {
        javax.swing.JOptionPane.showMessageDialog(this, "Error! Please enter reference.");
        jTextField3.requestFocus();
    }
    else if(jComboBox1.getSelectedIndex()==0)
    {
        javax.swing.JOptionPane.showMessageDialog(this, "Error! Please select supplier.");
        jComboBox1.requestFocus();
    }
    else if(jComboBox2.getSelectedIndex()==0)
    {
        javax.swing.JOptionPane.showMessageDialog(this, "Error! Please select purchase ledger.");
        jComboBox2.requestFocus();
    }
    else if(jTextField2.getText().isEmpty())
    {
        javax.swing.JOptionPane.showMessageDialog(this, "Error! Please enter TIN number.");
        jTextField2.requestFocus();
    }
    else if(jFormattedTextField2.getText().isEmpty())
    {
        javax.swing.JOptionPane.showMessageDialog(this, "Error! Please enter delivery date.");
        jFormattedTextField2.requestFocus();
    }
    else if(jComboBox5.getSelectedIndex() == 0)
    {
        javax.swing.JOptionPane.showMessageDialog(this, "Error! Please select Item Name.");
        jComboBox5.requestFocus();
    }

    else if(jTextField5.getText().trim().equals(""))
    {
        javax.swing.JOptionPane.showMessageDialog(this, "Error! Please enter Quantity.");
        jTextField5.requestFocus();
    }

    
    else if(jTextField6.getText().trim().equals(""))
    {
        javax.swing.JOptionPane.showMessageDialog(this, "Error! Please enter Rate.");
        jTextField6.requestFocus();
    }

    else if(jTextField7.getText().trim().equals(""))
    {
        javax.swing.JOptionPane.showMessageDialog(this, "Error! Please enter Discount %.");
        jTextField7.requestFocus();
    }
    else
    {
        addItemJTable();
    }
}

