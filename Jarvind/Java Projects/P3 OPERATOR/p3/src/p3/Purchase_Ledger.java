public void addItemJTable()
{
    jTextField1.setEnabled(false);
    jFormattedTextField1.setEnabled(false);
    jButton1.setEnabled(false);
    jTextField3.setEnabled(false);
    jComboBox1.setEnabled(false);
    jComboBox2.setEnabled(false);
    jTextField2.setEnabled(false);
    jFormattedTextField2.setEnabled(false);
    jButton2.setEnabled(false);
    String st="";
    if(jComboBox3.getSelectedIndex()>0)
        st=jComboBox3.getSelectedItem().toString();
    String item=jComboBox5.getSelectedItem().toString();
    String storage=st;
    int qty=Integer.parseInt(jTextField5.getText().trim());
    String unit=jComboBox4.getSelectedItem().toString();
    double rate=Double.valueOf(jTextField6.getText().trim());
    double total=qty*rate;
    double per=Double.valueOf(jTextField7.getText().trim());
    double dis=(total*per)/100;
    double total1=0;
    //double amt=total-dis;

    String arr[]=new String[9];
    arr[0]=""+(dtm.getRowCount()+1);
    arr[1]=item;
    arr[2]=storage;
    arr[3]=""+qty;
    arr[4]=unit;
    arr[5]=""+(new java.math.BigDecimal(Double.valueOf(rate)).setScale(2,java.math.BigDecimal.ROUND_HALF_UP));
    arr[6]=""+(new java.math.BigDecimal(Double.valueOf(per)).setScale(2,java.math.BigDecimal.ROUND_HALF_UP));
    arr[7]=""+(new java.math.BigDecimal(Double.valueOf(dis)).setScale(2,java.math.BigDecimal.ROUND_HALF_UP));
    arr[8]=""+(new java.math.BigDecimal(Double.valueOf(qty*rate-dis)).setScale(2,java.math.BigDecimal.ROUND_HALF_UP));

    dtm.addRow(arr);
    for(int i=0;i<dtm.getRowCount();i++)
    {
        total1=total1+Double.valueOf(jTable1.getValueAt(i, 8).toString());
    }
    jLabel10.setText(""+total1);
    javax.swing.JOptionPane.showMessageDialog(this, "Added successfully.");

    jComboBox5.requestFocus();
}

