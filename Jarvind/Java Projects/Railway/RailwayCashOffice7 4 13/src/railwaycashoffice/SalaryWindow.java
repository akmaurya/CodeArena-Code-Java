    private void panelShow()
    {
        if(controlOfPanelShow==1)
        {
            jButton1.setVisible(false);
            jButton2.setVisible(true);
            jButton4.setVisible(false);
            jPanel2.setVisible(true);
            jPanel3.setVisible(false);
            jPanel4.setVisible(false);
            jPanel5.setVisible(false);
            jTextField1.requestFocus();
        }
        else if(controlOfPanelShow==2)
        {
            jButton1.setVisible(true);
            jButton2.setVisible(true);
            jButton4.setVisible(false);
            jPanel3.setVisible(true);
            jPanel2.setVisible(false);
            jPanel4.setVisible(false);
            jPanel5.setVisible(false);
            jTextField9.requestFocus();
        }
        else if(controlOfPanelShow==3)
        {
            if(totalEarningCalculation())
            {
                jButton1.setVisible(true);
                jButton2.setVisible(true);
                jButton4.setVisible(false);
                jPanel2.setVisible(false);
                jPanel3.setVisible(false);
                jPanel4.setVisible(true);
                jPanel5.setVisible(false);
                jTextField19.requestFocus();
            }
            else
                controlOfPanelShow--;
        }
        else if(controlOfPanelShow==4)
        {
            if(totalDeductionCalculation())
            {
                jButton1.setVisible(true);
                jButton2.setVisible(false);
                jButton4.setVisible(true);
                jPanel2.setVisible(false);
                jPanel3.setVisible(false);
                jPanel4.setVisible(false);
                jPanel5.setVisible(true);
                showLastInfo();
                jButton4.requestFocus();
            }
            else
                controlOfPanelShow--;
        }
    }
    private void showLastInfo()
    {
        jLabel50.setText(jTextField1.getText().toUpperCase());
        jLabel46.setText(jLabel16.getText());
        jLabel47.setText(jLabel30.getText());
        double netAmt=(Double.valueOf(jLabel46.getText())-(Double.valueOf(jLabel47.getText())));
        jLabel48.setText(""+new java.math.BigDecimal(netAmt).setScale(2,5));
    }
