    private void jTextField21KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField21KeyReleased
        // TODO add your handling code here:
        totalDeductionCalculation();
        if(evt.getKeyChar()=='d' || evt.getKeyChar()=='f')
        {
            int temp=jTextField21.getText().indexOf("d");
            if(temp!=-1)
            {
                String str[]=jTextField21.getText().split("d");
                if(str!=null && str.length>0)
                    jTextField21.setText(str[0]);
            }
            if(temp==-1)
            {
                 temp=jTextField21.getText().indexOf("f");
            }
            if(temp!=-1)
            {
                String str[]=jTextField21.getText().split("f");
                if(str!=null && str.length>0)
                jTextField21.setText(str[0]);
            }
        }
    }//GEN-LAST:event_jTextField21KeyReleased

