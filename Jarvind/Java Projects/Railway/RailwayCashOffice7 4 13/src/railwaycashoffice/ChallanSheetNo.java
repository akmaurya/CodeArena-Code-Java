private boolean save()
{
    try
    {
        //interfaceRailway object1=(interfaceRailway)java.rmi.Naming.lookup("rmi://127.0.0.1/railwayMethods");
       interfaceRailway object1=null;
		try
		{
			object1=(interfaceRailway)java.rmi.Naming.lookup(getIPaddress.IP());
		}
		catch(Exception e)
		{
				System.out.println("Error at the time of connection with server "+getIPaddress.IP()+"..."+e);
				getIPaddress.changeIP();
				try
				{
					object1=(interfaceRailway)java.rmi.Naming.lookup(getIPaddress.IP());
				}
				catch(Exception ex)
				{
					System.out.println("Error at the time of connection with server ip-rmi//127.0.0.1/railwayMethods  ..."+ex);
				}
		}        
         if(jTextField1.getText().trim().equals(""))
         {
                javax.swing.JOptionPane.showMessageDialog(null,"Please input challan sheet no.");
                jTextField1.requestFocus();
                return false;
         }
         if(object1.isInteger(jTextField1.getText().trim())==false)
         {
             javax.swing.JOptionPane.showMessageDialog(null,"Error ! Please input number type(Challan sheet no.) value.");
             jTextField1.requestFocus();
             jTextField1.selectAll();
             return false;
         }


        
        
        /////////////Changes carried out here//////////////////////////////

        String qry="Select dp_code from department_detail where dp_name='"+jComboBox1.getSelectedItem()+"'";
        String getDpCode=object1.getSigetngleValue(qry,Main.year);
        qry="Select challansheet_no from challansheetno where  dp_code="+Integer.parseInt(getDpCode)+"";
        String getChallanSheetNo=object1.getSigetngleValue(qry,Main.year);
        if(getChallanSheetNo.length()==0)
        {
            qry="insert into challansheetno values("+Integer.parseInt(jTextField1.getText())+","+Integer.parseInt(getDpCode)+")";
            boolean bn=object1.saveInformation(qry,Main.year);
            if(bn)
            {
                javax.swing.JOptionPane.showMessageDialog(null,"Process Completed");
                jTextField1.setText("");
                jComboBox1.setSelectedIndex(0);
                jComboBox1.requestFocus();
            }
        }
        else if(getChallanSheetNo.length()>0)
        {
            qry="delete from challansheetno where dp_code="+Integer.parseInt(getDpCode);
            object1.saveInformation(qry,Main.year);
            qry="insert into challansheetno values("+Integer.parseInt(jTextField1.getText())+","+Integer.parseInt(getDpCode)+")";
            boolean bn=object1.saveInformation(qry,Main.year);
            if(bn)
            {
                javax.swing.JOptionPane.showMessageDialog(null,"Process Completed");
                jTextField1.setText("");
                jComboBox1.setSelectedIndex(0);
                jComboBox1.requestFocus();
            }


        }

    }
    catch(Exception e)
    {
        System.out.println("Error ! In save Method."+e);
    }
    return true;

}
