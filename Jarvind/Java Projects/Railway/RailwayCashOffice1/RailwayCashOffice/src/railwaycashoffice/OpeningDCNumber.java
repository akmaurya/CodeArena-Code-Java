private boolean Save()
{
    try
    {
       // interfaceRailway object1=(interfaceRailway)java.rmi.Naming.lookup("rmi://127.0.0.1/railwayMethods");
        interfaceRailway object1=null;
		try
		{
			object1=(interfaceRailway)java.rmi.Naming.lookup(getIPaddress.IP());
		}
		catch(Exception e)
		{
				System.out.println("Error at the time of connection with server "+getIPaddress.IP()+"..."+e);
				getIPaddress.changeIP();
				try
				{
					object1=(interfaceRailway)java.rmi.Naming.lookup(getIPaddress.IP());
				}
				catch(Exception ex)
				{
					System.out.println("Error at the time of connection with server ip-rmi//127.0.0.1/railwayMethods  ..."+ex);
				}
		}
        if(jTextField1.getText().trim().equals(""))
        {
            javax.swing.JOptionPane.showMessageDialog(null,"Please input DC number.");
            jTextField1.requestFocus();
            return false;
        }
        if(object1.isInteger(jTextField1.getText().trim())==false)
        {
            javax.swing.JOptionPane.showMessageDialog(null,"Please input DC number(Only number type value).");
            jTextField1.requestFocus();
            jTextField1.selectAll();
            return false;
        }

        String qry="Select dp_code from department_detail where dp_name='"+(""+jComboBox1.getSelectedItem())+"'";
        String getCode=object1.getSigetngleValue(qry,Main.year);
        if(getCode!=null)
        {
            qry="Select dp_Code from opening_dc_number where dp_Code="+Integer.parseInt(getCode)+"";
            String getDpCode=object1.getSigetngleValue(qry,Main.year);
          
            boolean bn=false;
            if(getDpCode==null || getDpCode.equals("")==true)
            {
                qry="insert into opening_dc_number values("+Integer.parseInt(getCode)+","+Integer.parseInt(jTextField1.getText().trim())+")";
                bn=object1.saveInformation(qry,Main.year);

            }
            else
            {
                qry="update opening_dc_number set DC_No="+Integer.parseInt(jTextField1.getText().trim())+" where dp_Code="+Integer.parseInt(getDpCode)+"";
                bn=object1.saveInformation(qry,Main.year);
            }
            if(bn)
            {
                    javax.swing.JOptionPane.showMessageDialog(null,"Process Completed.");
                    jTextField1.setText("");
                    jComboBox1.requestFocus();
                    UpLoadDetail();

            }
        }
    }
    catch(Exception e)
    {
        System.out.println("Error ! In Save Method."+e);
    }
    return true;

}
