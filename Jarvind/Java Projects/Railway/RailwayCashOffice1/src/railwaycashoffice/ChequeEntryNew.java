    private void jTextField4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyReleased
        // TODO add your handling code here:
         if(evt.getKeyChar()=='d' || evt.getKeyChar()=='f')
            {
                int temp=jTextField4.getText().indexOf("d");
                //System.out.println("temp.111........"+temp);
                if(temp!=-1)
                {
                    String str[]=jTextField4.getText().split("d");
                    if(str!=null && str.length>0)
                    jTextField4.setText(str[0]);
                    jTextField4.selectAll();
                    //  System.out.println("temp.inside........"+temp);

                }
                if(temp==-1)
                {
                    temp=jTextField4.getText().indexOf("f");
                  //  System.out.println("temp.222........"+temp);
                }
                if(temp!=-1)
                {
                    String str[]=jTextField4.getText().split("f");
                    if(str!=null && str.length>0)
                    jTextField4.setText(str[0]);
                    jTextField4.selectAll();
                }
            }
    }//GEN-LAST:event_jTextField4KeyReleased

