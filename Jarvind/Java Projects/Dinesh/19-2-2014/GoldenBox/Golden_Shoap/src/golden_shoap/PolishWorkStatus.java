    public boolean getData()
    {
        java.math.BigDecimal bd;
        if(jRadioButton1.isSelected()==false && jRadioButton2.isSelected()==false && jRadioButton3.isSelected()==false)
        {
            javax.swing.JOptionPane.showMessageDialog(this,"Please select any one option between polish pending or polish done or delevered");
            jRadioButton1.requestFocus();
            return false;
        }
        dtm.setRowCount(0);
        int status=5;
        if(jRadioButton1.isSelected())
        {
            status=0;
        }
        if(jRadioButton2.isSelected())
        {
            status=1;
        }
        if(jRadioButton3.isSelected())
        {
            status=2;
        }
        String qry=null;
        String date1=null,date2=null;
        String d[]=jLabel3.getText().split("-");
        date1=d[1]+"-"+d[0]+"-"+d[2];
        d=jLabel5.getText().split("-");
        date2=d[1]+"-"+d[0]+"-"+d[2];
        if(jCheckBox1.isSelected()==false)
        {
            //qry="Select distinct * from polish_details where status="+status+"";
            qry="Select distinct purja_number from polish_details where status="+status+"";
        }
        if(jCheckBox1.isSelected()==true)
        {
            //qry="Select distinct * from polish_details where status="+status+" and rec_date>=#"+date1+"# and rec_date<=#"+date2+"#";
            qry="Select distinct purja_number from polish_details where rec_date>=#"+date1+"# and rec_date<=#"+date2+"# and status="+status+"";

        }
        dtm.setRowCount(0);

        String load[]=Utility.getResultSetAs_1D_Array(qry);
        double total=0;
        String totamt="";
        int itemno=0;
        String arr[];
        if(load.length>0)
        {
            arr=new String[7];
            for(int i=0;i<load.length;i++)
            {
                            arr[0]=""+(i+1);
                            arr[1]=load[i];
                            qry="select ac_code from polish_details where purja_number="+(Integer.parseInt(arr[1]))+"";
                            String code=""+Utility.getSingleValue(qry);
                            qry="select ac_name from ac_master where ac_code="+(Integer.parseInt(code))+"";
                            String name=""+Utility.getSingleValue(qry);
                            arr[2]=name;
                            qry="Select distinct rec_date from polish_details where purja_number="+(Integer.parseInt(arr[1]))+"";
                            arr[3]=Utility.getSingleValue(qry);
                            String da[]=arr[3].split(" ");
                            String da1[]=da[0].split("-");
                            arr[3]=da1[2]+"-"+da1[1]+"-"+da1[0];
                            //System.out.println("Date="+arr[2]);
                            String q1="select amount from polish_details where purja_number="+Integer.parseInt(arr[1])+"";
                            String la[]=Utility.getResultSetAs_1D_Array(q1);
                            double amt=0;
                            if(la.length>0)
                            {
                                for(int k=0;k<la.length;k++)
                                {
                                    amt=Double.valueOf(la[k])+amt;
                                }
                            }
                            java.math.BigDecimal bd1=new java.math.BigDecimal(amt);
                            arr[4]=(""+bd1.setScale(2,5));
                            qry="select distinct discount from polish_details where purja_number="+Integer.parseInt(arr[1])+"";
                            arr[5]=Utility.getSingleValue(qry);
                            bd1=new java.math.BigDecimal(arr[5]);
                            arr[5]=(""+bd1.setScale(2,5));

                            qry="select voucher_amount from receive_voucher where purja_number="+Integer.parseInt(arr[1])+" and voucher_type='Receive'";
                            String paidAmount=Utility.getSingleValue(qry);
                            if(paidAmount==null || paidAmount.isEmpty()==true)
                            {
                                arr[6]="0.00";
                            }
                            else
                            {
                                bd1=new java.math.BigDecimal(paidAmount);
                                arr[6]=(""+bd1.setScale(2,5));
                            }

                            dtm.addRow(arr);

            }
         }
  return true;
    }
