    public boolean add()
    {
         try
        {      double newOpeningStock=Integer.parseInt(jTextField1.getText());
               String selectedItem=""+jComboBox1.getSelectedItem();
               String qry="select item_quantity,opening_stock from stock_master where item_name='"+selectedItem+"'";
               String[][] data=Utility.getInformationDD(qry,2);
               double oldQuantity=Integer.parseInt(data[0][0]);
               double oldOpeningStock=Integer.parseInt(data[0][1]);
               double newQuantity=((oldQuantity-oldOpeningStock)+newOpeningStock);
               if((jTextField1.getText()).equals(""))
               {
                    javax.swing.JOptionPane.showMessageDialog(this,"Opening stock does not blank.Minimum zero is required." ,"Message Box",javax.swing.JOptionPane.INFORMATION_MESSAGE);

               }

               else
               {
                    String qry1="update stock_master set item_quantity="+newQuantity+",opening_stock="+newOpeningStock+" where item_name='"+selectedItem+"'";
                    Utility.saveInformation(qry1);
                    javax.swing.JOptionPane.showMessageDialog(this,"Record sucessfully updated." ,"Message Box",javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    jComboBox1.setSelectedItem("Select");
                    jTextField1.setText("");
               }

        }
        catch(NumberFormatException ex)
        {
                 javax.swing.JOptionPane.showMessageDialog(this,"Entry is not valied." ,"Message Box",javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
         return true;
    }
