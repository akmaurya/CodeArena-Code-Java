    private void loadini()
    {
        try
        {
            String qry="Select Plane_Id from Master_planeDetail";
            String load[]=Utility.getResultSetAs_1D_Array(qry);
            if(load!=null)
            {
                jComboBox2.removeAllItems();
                for(int i=0;i<load.length;i++)
                {
                    jComboBox2.addItem(load[i]);
                    System.out.println(load[i]);
                }
            }
        }
        catch(Exception ae)
        {
            System.out.println("Error in loadini method........."+ae);
        }
    }
    private void load()
    {
        try
        {
            String qry="Select * from master_user ";
            String load[][]=Utility.getInformationDD(qry,8);
            String arr[]=null;
            if(load.length>0)
            {
                arr=new String[9];
                for(int i=0;i<load.length;i++)
                {
                    arr[0]=""+(dtm.getRowCount()+1);
                    arr[1]=load[i][0];
                    arr[2]=load[i][1];
                    arr[3]=load[i][2];
                    arr[4]=load[i][3];
                    arr[5]=load[i][4];
                    arr[6]=load[i][5];
                    arr[7]=load[i][6];
                    arr[8]=load[i][7];
                    dtm.addRow(arr);
                }
            }
        }
        catch(Exception ae)
        {
            System.out.println("Error in load method........."+ae);
        }
    }
