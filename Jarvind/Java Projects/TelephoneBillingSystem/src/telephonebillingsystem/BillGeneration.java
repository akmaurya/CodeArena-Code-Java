    private void load()
    {
        try
        {
            Double total=0.0,pre=0.0,use=0.0,cur=0.0;
            String qry="Select * from MasterBillCreation";
            String load[][]=Utility.getInformationDD(qry,9);
            String arr[]=null;
            if(load.length>0)
            {
                arr=new String[10];
                for(int i=0;i<load.length;i++)
                {
                    arr[0]=""+(dtm.getRowCount()+1);
                    arr[1]=load[i][0];
                    arr[2]=load[i][1];
                    arr[3]=load[i][2];
                    pre=Double.valueOf(arr[3]);
                    arr[4]=load[i][3];
                    use=Double.valueOf(arr[4]);
                    arr[5]=load[i][4];
                    cur=Double.valueOf(arr[5]);
                    String D[]=load[i][5].split("-");
                    String dd[]=D[2].split(" ");//+"-"+D[1]+"-"+D[0];
                    String setDate=dd[0]+"-"+D[1]+"-"+D[0];
                    arr[6]=setDate;

                    D=load[i][6].split("-");
                    dd=D[2].split(" ");//+"-"+D[1]+"-"+D[0];
                    setDate=dd[0]+"-"+D[1]+"-"+D[0];
                    arr[7]=setDate;
                    arr[8]=load[i][7];
                    arr[9]=""+(pre+use+cur);
                    dtm.addRow(arr);
                }
            }
        }
        catch(Exception ae)
        {
            System.out.println("Error in load method........."+ae);
        }
    }
