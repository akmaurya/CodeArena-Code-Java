    private void load()
    {
        try
        {
            String qry="Select * from Master_Data_Detail ";
            String load[][]=Utility.getInformationDD(qry,6);
            String arr[]=null;
            if(load.length>0)
            {
                arr=new String[7];
                for(int i=0;i<load.length;i++)
                {
                    arr[0]=""+(dtm.getRowCount()+1);
                    arr[1]=load[i][0];
                    arr[2]=load[i][1];
                    arr[3]=load[i][2];
                    arr[4]=load[i][3];
                    arr[5]=load[i][4];
                    arr[6]=load[i][5];
                    dtm.addRow(arr);
                }
            }
        }
        catch(Exception ae)
        {
            System.out.println("Error in load method........."+ae);
        }
    }
    public void save()
    {
        try
        {
            dtm.setRowCount(0);
            String qry="delete * from Master_Data_Detail";
            boolean bn1=Utility.saveInformation(qry);
            if(bn1==true)
            {
                qry="insert into Master_Data_Detail values ("+Double.valueOf(jTextField4.getText().trim())+","+Double.valueOf(jTextField3.getText().trim())+","+Double.valueOf(jTextField1.getText().trim())+","+Double.valueOf(jTextField5.getText().trim())+","+Double.valueOf(jTextField6.getText().trim())+")";
                boolean bn=Utility.saveInformation(qry);
                if(bn==true)
                {
                    javax.swing.JOptionPane.showMessageDialog(null,"Process Completed.....");
                    load();
                    jTextField1.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                    jTextField6.setText("");
                    jTextField4.requestFocus();
                }
            }
        }
        catch(Exception ae)
        {
            System.out.println("Error in save method"+ae);
        }
    }
