class MoveSlider extends Thread
{
    public void run()
    {
        for(int i=1;i<=total;i++)
        {
            try
            {
                jProgressBar1.setValue(i);
                Thread.sleep(1000);
            }

            catch(Exception ex)
            {
                System.out.println("MoveSlider : "+ex.getMessage());
            }
        }
        
        jProgressBar1.setValue(0);
    }
}

class SetTime extends Thread
{
    public void run()
    {
        for(int m=0;m<=min;m++)
        {
            for(int s=0;s<60;s++)
            {
                if(m/10==0 && s/10==0)
                {
                    jLabel6.setText("0"+m+" : "+"0"+s);
                }
                
                if(m/10==0 && s/10!=0)
                {
                    jLabel6.setText("0"+m+" : "+s);
                }
                
                if(m/10!=0 && s/10==0)
                {
                    jLabel6.setText(m+" : "+"0"+s);
                }
                
                if(m/10!=0 && s/10!=0)
                {
                    jLabel6.setText(m+" : "+s);
                }
                
                try
                {
                    Thread.sleep(1000);
                }

                catch(Exception ex)
                {
                    System.out.println("SetTime : "+ex.getMessage());
                }
                
                cl1=new Color(0,0,204);
                cl2=new Color(0,153,0);
    
                if(s%2!=0)
                {
                    jButton6.setForeground(cl1);
                    jButton1.setBackground(cl1);
                    jLabel3.setForeground(cl1);
                }

                else
                {
                    jButton6.setForeground(cl2);
                    jButton1.setBackground(cl2);
                    jLabel3.setForeground(cl2);
                }
                
                if(m==min && s==sec)
                {
                    jMenuItem1.setEnabled(true);
                    jButton8.setEnabled(true);
                    jButton6.setEnabled(true);
                    break;
                }
            }
        }
    }
}

public void choose_Wallpaper()
{
    try
    {
        JFileChooser jFileChooser1=new JFileChooser();
        jFileChooser1.showOpenDialog(this);
        f5=jFileChooser1.getSelectedFile();

        if((f5.toString()).equals("null"))
            jTextField1.setText("");

        else
        {
            String par_name=f5.getParent();
            f6=new File(par_name);

            name2=f6.listFiles();

            ChangeBackground back=new ChangeBackground();
            back.start();
        }
    }
    
    catch(Exception ex)
    {
        
    }
}

class ChangeBackground extends Thread
{
    public void run()
    {
        for(int i=0;i<name2.length;i++)
        {
            ImageIcon icon=new ImageIcon(""+name2[i]);
            jLabel1.setIcon(icon);
            
            try
            {
                Thread.sleep(2500);
            }

            catch(Exception ex)
            {
                System.out.println("ChangeBackground : "+ex.getMessage());
            }
            
            if(i==name2.length)
            {
                i=0;
            }
        }
    }
}
