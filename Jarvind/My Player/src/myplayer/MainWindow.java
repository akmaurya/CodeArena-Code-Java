public void save_Image()
{
    try
    {
        JFileChooser jFileChooser1=new JFileChooser();
        jFileChooser1.showSaveDialog(this);
        f5=new File(jFileChooser1.getSelectedFile().getPath());
        
        if((f5.toString()).equals("null"))
        jTextField1.setText("");
        
        else
        {
            BufferedImage image=ImageIO.read(f5); //read a file
        
            String fname=javax.swing.JOptionPane.showInputDialog(this, "Enter the file name : ");
            String format=javax.swing.JOptionPane.showInputDialog(this, "Enter the file format : ");

            f6=new File(fname+"."+format); //file name with extension
            ImageIO.write(image, format, f6); //write a file

            javax.swing.JOptionPane.showMessageDialog(this, "Saved Successfully.");

            int c=javax.swing.JOptionPane.showConfirmDialog(this, "Do you want to save another image?");

            if(c==0)
            {
                save_Image();
            }

            if(c==1)
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Thanks for using My Player | Simple Player.");
            }
        }
    }

    catch(Exception ex)
    {
        
    }
}

