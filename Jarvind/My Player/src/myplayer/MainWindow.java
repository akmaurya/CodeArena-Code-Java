public void play_audio() throws Exception
{
    song_name=jTextField1.getText().substring(jTextField1.getText().lastIndexOf('\\')+1, jTextField1.getText().length());
    
    f=new File(jTextField1.getText());
    Player p=Manager.createRealizedPlayer(f.toURI().toURL());
    
    p.start();
    
    jMenuItem1.setEnabled(false);
    jButton8.setEnabled(false);
    jButton6.setEnabled(false);
    jButton1.setEnabled(false);
    
    f2=new File(jTextField1.getText());
    String par_name=f2.getParent();
    f3=new File(par_name);
    name=f3.listFiles();
    
    Time t2=p.getDuration();
    double total_time=t2.getSeconds();
    total=(int)total_time;
    
    System.out.println("Total running time : "+total+" seconds");
    
    jLabel3.setText("Now playing : "+song_name);
    jProgressBar1.setStringPainted(true);
    jProgressBar1.setString(song_name);
    
    jProgressBar1.setMinimum(0);
    jProgressBar1.setMaximum(total);
    
    sec=total%60;
    min=total/60;
    
    System.out.println("Minutes : "+min+" minutes");
    System.out.println("Seconds : "+sec+" seconds");
    
    if(min/10==0 && sec/10==0)
    {
        jLabel5.setText("0"+min+" : "+"0"+sec);
    }

    if(min/10==0 && sec/10!=0)
    {
        jLabel5.setText("0"+min+" : "+sec);
    }

    if(min/10!=0 && sec/10==0)
    {
        jLabel5.setText(min+" : "+"0"+sec);
    }

    if(min/10!=0 && sec/10!=0)
    {
        jLabel5.setText(min+" : "+sec);
    }
    
    SetTime time=new SetTime();
    time.start();
    
    MoveSlider move_slider=new MoveSlider();  
    move_slider.start();
}

